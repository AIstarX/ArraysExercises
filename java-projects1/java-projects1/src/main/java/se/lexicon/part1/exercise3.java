package se.lexicon.part1;

public class exercise3 {
    public static void main(String[] args) {
        //Numbers for operations
        int num1 = 45;
        int num2 = 11;
        int num3 = 12;
        int num4 = 4;
        int num5 = 24;
        int num6 = 5;
        int num7 = 55;
        int num8 = 12;

        //Performance the operations
        int add = num1 + num2;
        int product = num3 * num4;
        int division = num5 / num6;
        int subtraction = num7 - num8;

        //Print the results
        System.out.println(num1 + " + " + num2 + " = " + add);
        System.out.println(num3 + " * " + num4 + " = " + product);
        System.out.println(num5 + " / " + num5 + " = " + division);
        System.out.println(num7 + " - " + num8 + " = " + subtraction);

    }
}
